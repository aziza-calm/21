#include "libft.h"

void	ft_putnbr(int nb)
{
	int i;
	int f;

	i = 1000000000;
	f = 1;
	if (nb < 0)
	{
		ft_putchar('-');
		f = -1;
	}
	if (nb == 0)
		ft_putchar('0');
	else
		while (nb / i == 0)
			i = i / 10;
	while (nb != 0)
	{
		ft_putchar(48 + f * (nb / i));
		nb = nb % i;
		i = i / 10;
	}
}
