#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	*p;

	i = -1;
	p = b;
	while (++i < len)
		*(p + i) = (unsigned char)c;
	return (b);
}
